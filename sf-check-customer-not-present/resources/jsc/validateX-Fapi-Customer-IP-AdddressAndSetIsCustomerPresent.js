// Check that x-fapi-customer-ip-address contains a valid IP address
// and in that case, set isCustomerPresent to true
 
var fapiIpAddress = context.getVariable("request.header.x-fapi-customer-ip-address");
var foundValidIPv4AddrArray = fapiIpAddress.match(/^((25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/g);
var isCustomerPresentWithIpv4 = (foundValidIPv4AddrArray !== null) && (foundValidIPv4AddrArray.length === 1);
var foundValidIPv6AddrArray = fapiIpAddress.match(/^(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))$/g);
var isCustomerPresentWithIpv6 = (foundValidIPv6AddrArray !== null) && (foundValidIPv6AddrArray.length === 1);

var isCustomerPresent = false;
if (isCustomerPresentWithIpv4 || isCustomerPresentWithIpv6) {
    isCustomerPresent = true;
} 
context.setVariable("isCustomerPresent", isCustomerPresent);
 
 